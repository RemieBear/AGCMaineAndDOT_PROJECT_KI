# PREPROCESSING - to prepare text for TF-IDF Vectorization

def preprocess_text(text):

    if not isinstance(text, str): return

    # Lowercase conversion
    text = text.lower()

    # Remove punctuation
    text = re.sub(r'[^\w\s]', '', text)

    # Tokenization
    tokens = text.split()

    # Remove stop wordsand Lemmatization
    cleaned = [lemmatizer.lemmatize(w) for w in tokens if w not in stop_words]

    # Re-join tokens into a single 'Skill Document'.
    return " ".join(cleaned)

# Create the 'Master Skill Document' for each job role
# We combine the Title, Roles, and Keywords for clustering
onet_data_cleaned['cleaned_text'] = (onet_data_cleaned['Title'].fillna('') + " " +
                                  onet_data_cleaned['Key Roles'].fillna('') + " " +
                                  onet_data_cleaned['Key Words'].fillna('')).apply(preprocess_text)

