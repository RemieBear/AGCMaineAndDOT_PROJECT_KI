# BRIDGE ROLE ANALYSIS (TALENT PIVOTS)
logger.info("Identifying Bridge Roles...")

# Identify the 'Management' cluster by finding where 'Manager' titles appear most
mask_mgmt = onet_data_cleaned['Title'].str.contains('Manager|Management', case=False, na=False)
mgmt_cluster_id = onet_data_cleaned[mask_mgmt]['Cluster'].mode()[0] if mask_mgmt.any() else 0

# Dynamically find the Trade Cluster
all_clusters = set(onet_data_cleaned['Cluster'].unique())
other_clusters = list(all_clusters - {mgmt_cluster_id})

# To check which of the remaining clusters contains 'Highway' or 'Maintenance'
mask_trade = onet_data_cleaned['Title'].str.contains('Highway|Maintenance|Equipment', case=False, na=False)
if mask_trade.any():
    trade_cluster_id = onet_data_cleaned[mask_trade]['Cluster'].mode()[0]
else:
    trade_cluster_id = other_clusters[0] # Fallback to the first non-management cluster

logger.info(f"Dynamic Detection: Mgmt Cluster={mgmt_cluster_id}, Trade Cluster={trade_cluster_id}")


# Calculate the Management Centroid
mgmt_vectors = tfidf_matrix[(onet_data_cleaned['Cluster'] == mgmt_cluster_id).values]
mgmt_centroid = np.asarray(mgmt_vectors.mean(axis=0))

# Calculate Cosine Similarity
trade_mask = (onet_data_cleaned['Cluster'] == trade_cluster_id).values
trade_vectors = tfidf_matrix[trade_mask]
similarities = cosine_similarity(trade_vectors, mgmt_centroid).flatten()

# Create the "Bridge Role" Leaderboard
bridge_df = pd.DataFrame({
    'Title': onet_data_cleaned[trade_mask]['Title'],
    'Similarity_to_Mgmt': similarities
}).sort_values(by='Similarity_to_Mgmt', ascending=False)

print("\n Top Bridge Roles (Trade to Management)")
print(bridge_df.head(3))



